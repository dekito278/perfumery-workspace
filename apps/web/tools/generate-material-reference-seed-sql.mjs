#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import {
  chunkArray,
  ensureDirectoryExists,
  parseArgs,
  readJsonFile,
} from './material-reference-common.mjs';

const DEFAULT_EXPORT_DIR = path.resolve('../../docs/material-reference-export');
const DEFAULT_MIGRATION_NAME = '20260419190000_seed_material_reference_data.sql';

const ABC_COLUMNS = ['letter', 'class_index', 'family_name', 'description'];
const PROFILE_COLUMNS = [
  'reference_code',
  'name',
  'synonym',
  'supplier',
  'abc_code',
  'abc_primary_letter',
  'abc_primary_family',
  'abc_secondary_letter',
  'abc_secondary_family',
  'category',
  'catalog_tag',
  'classification',
  'brief_description',
  'odour_description',
  'odour_profile',
  'perfume_uses',
  'flavour_uses',
  'function_labels',
  'function_raw',
  'order_no',
  'impact',
  'life_hours',
  'use_level_min_percent',
  'use_level_typical_percent',
  'use_level_max_percent',
  'ifra_limit_percent',
  'stability_heat',
  'stability_discolour',
  'stability_storage',
  'stability_antioxidant',
  'stability_summary',
  'physical_state',
  'mol_formula',
  'molecular_weight',
  'cas_no',
  'safety',
  'ifra',
  'pw_price',
  'catalog_price',
  'catalog_available',
  'catalog_unit',
  'source_workbook_path',
  'source_catalog_path',
  'raw_payload',
];
const FACET_COLUMNS = ['reference_code', 'letter', 'family', 'value', 'description', 'sort_order'];

const sqlLiteral = (value, forceJson = false) => {
  if (value === null || value === undefined) {
    return 'NULL';
  }

  if (forceJson) {
    const json = JSON.stringify(value ?? null).replace(/'/g, "''");
    return `'${json}'::jsonb`;
  }

  if (typeof value === 'number') {
    return Number.isFinite(value) ? String(value) : 'NULL';
  }

  if (typeof value === 'boolean') {
    return value ? 'TRUE' : 'FALSE';
  }

  const stringValue = String(value).replace(/'/g, "''");
  return `'${stringValue}'`;
};

const sanitizePercentValue = (value) => {
  if (value === null || value === undefined || value === '') {
    return null;
  }

  const numericValue = Number(value);
  if (!Number.isFinite(numericValue) || numericValue < 0 || numericValue > 100) {
    return null;
  }

  return numericValue;
};

const buildValuesSql = (rows, columns, options = {}) =>
  rows.map((row) => {
    const values = columns.map((column) =>
      sqlLiteral(row[column], options.jsonColumns?.includes(column)),
    );
    return `  (${values.join(', ')})`;
  }).join(',\n');

const buildUpsertAssignments = (columns, alias = 'EXCLUDED') =>
  columns
    .filter((column) => column !== 'reference_code' && column !== 'letter')
    .map((column) => `${column} = ${alias}.${column}`)
    .join(',\n  ');

const main = () => {
  const args = parseArgs(process.argv.slice(2));
  const exportDir = path.resolve(String(args.get('export-dir') || DEFAULT_EXPORT_DIR));
  const migrationName = String(args.get('name') || DEFAULT_MIGRATION_NAME);
  const outputPath = path.resolve(
    String(args.get('out') || path.join('../../supabase', 'migrations', migrationName)),
  );

  const abcFamilies = readJsonFile(path.join(exportDir, 'abc-families.json'));
  const profiles = readJsonFile(path.join(exportDir, 'reference-profiles.json')).map((profile) => ({
    ...profile,
    use_level_min_percent: sanitizePercentValue(profile.use_level_min_percent),
    use_level_typical_percent: sanitizePercentValue(profile.use_level_typical_percent),
    use_level_max_percent: sanitizePercentValue(profile.use_level_max_percent),
    ifra_limit_percent: sanitizePercentValue(profile.ifra_limit_percent),
  }));
  const facets = readJsonFile(path.join(exportDir, 'reference-odour-facets.json'));

  const lines = [];
  lines.push('-- Generated by apps/web/tools/generate-material-reference-seed-sql.mjs');
  lines.push('-- Source: docs/material-reference-export');
  lines.push('BEGIN;');
  lines.push('');

  for (const chunk of chunkArray(abcFamilies, 26)) {
    lines.push('INSERT INTO public.material_reference_abc_families (letter, class_index, family_name, description)');
    lines.push('VALUES');
    lines.push(buildValuesSql(chunk, ABC_COLUMNS));
    lines.push('ON CONFLICT (letter) DO UPDATE SET');
    lines.push('  class_index = EXCLUDED.class_index,');
    lines.push('  family_name = EXCLUDED.family_name,');
    lines.push('  description = EXCLUDED.description;');
    lines.push('');
  }

  for (const chunk of chunkArray(profiles, 100)) {
    lines.push(`INSERT INTO public.material_reference_profiles (${PROFILE_COLUMNS.join(', ')})`);
    lines.push('VALUES');
    lines.push(buildValuesSql(chunk, PROFILE_COLUMNS, { jsonColumns: ['raw_payload'] }));
    lines.push('ON CONFLICT (reference_code) DO UPDATE SET');
    lines.push(`  ${buildUpsertAssignments(PROFILE_COLUMNS)};`);
    lines.push('');
  }

  lines.push('DELETE FROM public.material_reference_odour_facets;');
  lines.push('');

  for (const chunk of chunkArray(facets, 250)) {
    lines.push(`WITH facet_source (${FACET_COLUMNS.join(', ')}) AS (`);
    lines.push('  VALUES');
    lines.push(buildValuesSql(chunk, FACET_COLUMNS));
    lines.push(')');
    lines.push('INSERT INTO public.material_reference_odour_facets (reference_profile_id, letter, family, value, description, sort_order)');
    lines.push('SELECT profile.id, facet.letter, facet.family, facet.value, facet.description, facet.sort_order');
    lines.push('FROM facet_source AS facet');
    lines.push('JOIN public.material_reference_profiles AS profile');
    lines.push('  ON profile.reference_code = facet.reference_code;');
    lines.push('');
  }

  lines.push('COMMIT;');
  lines.push('');

  ensureDirectoryExists(path.dirname(outputPath));
  fs.writeFileSync(outputPath, lines.join('\n'), 'utf8');

  console.log(JSON.stringify({
    output_path: outputPath,
    abc_family_records: abcFamilies.length,
    reference_records: profiles.length,
    odour_facet_records: facets.length,
  }, null, 2));
};

main();
